#include "libft.h"
#include "push_swap.h"

int	check_order(t_list *head)
{
	t_list	*temp;

	temp = head;
	while (head->next)
	{
		if (head->content > head->next->content)
			return (0);
		head = head->next;
	}
	return (1);
}
